export function getCaseById(id: string): Case | undefined {
    return seedCases.find((c) => c.id === id);
}

export function getCasesByStatus(status: CaseStatus): Case[] {
    return seedCases.filter((c) => c.status === status);
}

export function getCasesByPriority(priority: CasePriority): Case[] {
    return seedCases.filter((c) => c.priority === priority);
}

export function getCasesByCategory(category: CaseCategory): Case[] {
    return seedCases.filter((c) => c.category === category);
}

export function getCaseStats() {
    return {
        total: seedCases.length,
        open: seedCases.filter((c) => c.status === "open").length,
        active: seedCases.filter((c) => c.status === "active").length,
        pending: seedCases.filter((c) => c.status === "pending" || c.status === "under_review").length,
        closed: seedCases.filter((c) => c.status === "closed").length,
        cold: seedCases.filter((c) => c.status === "cold").length,
        critical: seedCases.filter((c) => c.priority === "critical").length,
        clearanceRate: Math.round(
            (seedCases.filter((c) => c.status === "closed").length / seedCases.length) * 100
        ),
    };
}
